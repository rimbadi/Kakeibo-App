package com.example.skripsi.Data;

public class DataKategori {
    private String nama_kategori, budget_kategori;
    public DataKategori(){
        this.nama_kategori = nama_kategori;
        this.budget_kategori = budget_kategori;
    }
    public String getNama_kategori() {
        return nama_kategori;
    }
    public void setNama_kategori(String nama_kategori) {
        this.nama_kategori = nama_kategori;
    }
    public String getBudget_kategori() {
        return budget_kategori;
    }
    public void setBudget_kategori(String budget_kategori) {
        this.budget_kategori = budget_kategori;
    }
}
