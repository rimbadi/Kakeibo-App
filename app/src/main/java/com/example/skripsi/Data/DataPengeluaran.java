package com.example.skripsi.Data;

public class DataPengeluaran {
    public String id_pengeluaran, kategori_pengeluaran, keterangan_pengeluaran, uang_pengeluaran, tanggal_pengeluaran;
    public DataPengeluaran(){
        this.id_pengeluaran = id_pengeluaran;
        this.kategori_pengeluaran = kategori_pengeluaran;
        this.keterangan_pengeluaran = keterangan_pengeluaran;
        this.uang_pengeluaran = uang_pengeluaran;
        this.tanggal_pengeluaran = tanggal_pengeluaran;
    }

    public String getId_pengeluaran() {
        return id_pengeluaran;
    }
    public void setId_pengeluaran(String id_pengeluaran) {
        this.id_pengeluaran = id_pengeluaran;
    }
    public String getKategori_pengeluaran() {
        return kategori_pengeluaran;
    }
    public void setKategori_pengeluaran(String kategori_pengeluaran) {
        this.kategori_pengeluaran = kategori_pengeluaran;
    }
    public String getKeterangan_pengeluaran() {
        return keterangan_pengeluaran;
    }
    public void setKeterangan_pengeluaran(String keterangan_pengeluaran) {
        this.keterangan_pengeluaran = keterangan_pengeluaran;
    }
    public String getUang_pengeluaran() {
        return uang_pengeluaran;
    }
    public void setUang_pengeluaran(String uang_pengeluaran) {
        this.uang_pengeluaran = uang_pengeluaran;
    }
    public String getTanggal_pengeluaran() {
        return tanggal_pengeluaran;
    }
    public void setTanggal_pengeluaran(String tanggal_pengeluaran) {
        this.tanggal_pengeluaran = tanggal_pengeluaran;
    }
}

